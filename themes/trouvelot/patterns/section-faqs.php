<p class="has-normal-font-size"><strong>Why are links not allowed in reviews?</strong></p>
<!-- /wp:paragraph --></div>
<!-- /wp:column -->

<!-- wp:column {"width":"70%"} -->
<div class="wp-block-column" style="flex-basis:70%"><!-- wp:paragraph -->
<p>To reduce the overhead of needing to constantly re-check reviews, links and so forth for our volunteers, we made the decision that links should not be used in reviews. If we do notice these, any such link will be removed or the review will be archived, at the discretion of the moderators. If someone repeatedly puts in links in a review, their account will be suspended.</p>
